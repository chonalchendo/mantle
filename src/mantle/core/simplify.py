# ── Private helpers ──────────────────────────────────────────────


def _verify_issue_exists(project_root: Path, issue: int) -> None:
    """Raise FileNotFoundError if the issue file does not exist."""
    issue_path = issues.find_issue_path(project_root, issue)
    if issue_path is None:
        msg = f"Issue {issue} not found"
        raise FileNotFoundError(msg)
    issues.load_issue(issue_path)


def _grep_issue_commits(project_root: Path, issue: int) -> list[str]:
    """Return commit hashes for commits referencing the given issue.

    Searches for ``(issue-N)`` in commit messages, and for N < 10
    also searches ``(issue-0N)`` to catch zero-padded history.

    Args:
        project_root: Directory containing the git repo.
        issue: Issue number to search for.

    Returns:
        List of commit hashes, most-recent first, deduplicated.
    """

    def _grep(pattern: str) -> list[str]:
        result = subprocess.run(
            ["git", "log", "--oneline", f"--grep={pattern}", "--format=%H"],
            capture_output=True,
            text=True,
            check=True,
            cwd=project_root,
        )
        return result.stdout.strip().splitlines()

    commit_hashes = _grep(f"(issue-{issue})")

    if issue < 10:
        padded = _grep(f"(issue-0{issue})")
        seen = set(commit_hashes)
        commit_hashes += [h for h in padded if h not in seen]

    return commit_hashes


# ── Public API ───────────────────────────────────────────────────


def collect_issue_files(
    project_root: Path,
    issue: int,
) -> tuple[str, ...]:
    """Collect files changed by an issue's implementation.

    Loads the issue to verify it exists, then finds commits
    matching the conventional commit pattern ``feat(issue-N):``
    and collects all files changed across those commits.

    For single-digit issues (N < 10), also searches for
    zero-padded forms like ``(issue-01)`` to catch early
    project history.

    Args:
        project_root: Directory containing .mantle/.
        issue: Issue number to collect files for.

    Returns:
        Tuple of file paths relative to project root,
        or empty tuple if no matching commits exist.

    Raises:
        FileNotFoundError: If the issue file does not exist.
    """
    _verify_issue_exists(project_root, issue)
    commit_hashes = _grep_issue_commits(project_root, issue)

    if not commit_hashes:
        return ()

    first_commit = commit_hashes[-1]
    last_commit = commit_hashes[0]

    diff_result = subprocess.run(
        ["git", "diff", "--name-only", f"{first_commit}^..{last_commit}"],
        capture_output=True,
        text=True,
        check=True,
        cwd=project_root,
    )

    files = [f for f in diff_result.stdout.strip().splitlines() if f]
    return tuple(files)


def collect_issue_diff_stats(
    project_root: Path,
    issue: int,
) -> DiffStats:
    """Aggregate diff stats across commits for an issue.

    Uses the same commit-discovery logic as
    :func:`collect_issue_files` (grep for ``(issue-N)`` and, for
    ``N < 10``, also ``(issue-0N)``), then runs
    ``git diff --shortstat <first>^..<last> -- src/ tests/`` and
    parses the single summary line. Counts only changes in ``src/``
    and ``tests/`` — aligns with the simplifier's edit scope in
    ``/mantle:build`` Step 7.

    Args:
        project_root: Directory containing .mantle/.
        issue: Issue number to collect diff stats for.

    Returns:
        DiffStats with file count and line totals. Returns
        ``DiffStats(0, 0, 0, 0)`` when no matching commits exist,
        and also when matching commits exist but touch only paths
        outside ``src/`` + ``tests/``.

    Raises:
        FileNotFoundError: If the issue file does not exist.
    """
    _verify_issue_exists(project_root, issue)
    commit_hashes = _grep_issue_commits(project_root, issue)

    if not commit_hashes:
        return DiffStats(0, 0, 0, 0)

    first_commit = commit_hashes[-1]
    last_commit = commit_hashes[0]

    shortstat = subprocess.run(
        [
            "git",
            "diff",
            "--shortstat",
            f"{first_commit}^..{last_commit}",
            "--",
            "src/",
            "tests/",
        ],
        capture_output=True,
        text=True,
        check=True,
        cwd=project_root,
    )

    match = _SHORTSTAT_RE.search(shortstat.stdout)
    if match is None:
        return DiffStats(0, 0, 0, 0)

    files = int(match.group(1))
    lines_added = int(match.group(2)) if match.group(2) else 0
    lines_removed = int(match.group(3)) if match.group(3) else 0
    return DiffStats(
        files=files,
        lines_added=lines_added,
        lines_removed=lines_removed,
        lines_changed=lines_added + lines_removed,
    )


def collect_changed_files(
    project_root: Path,
) -> tuple[str, ...]:
    """Collect all changed files in the working tree.

    Includes staged changes, unstaged changes, and untracked
    files (excluding ignored files).

    Args:
        project_root: Directory containing the git repo.

    Returns:
        Tuple of file paths relative to project root.
    """
    diff_result = subprocess.run(
        ["git", "diff", "--name-only", "HEAD"],
        capture_output=True,
        text=True,
        check=True,
        cwd=project_root,
    )

    untracked_result = subprocess.run(
        ["git", "ls-files", "--others", "--exclude-standard"],
        capture_output=True,
        text=True,
        check=True,
        cwd=project_root,
    )

    all_files = {
        line
        for output in (diff_result.stdout, untracked_result.stdout)
        for line in output.strip().splitlines()
        if line
    }

    return tuple(sorted(all_files))
